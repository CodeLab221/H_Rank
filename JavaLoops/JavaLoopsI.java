import java.util.*;


public class Solution {

   public static void main(String[] args){
           Scanner sc = new Scanner(System.in);


           int N = sc.nextInt();

           for(int A=0; A<=10; A++) {
                           
           
           int S = N*A;
           

          System.out.println(N + " x " + A +" = "+ S );
    }


  }

}
